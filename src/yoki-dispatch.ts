import {
  MultiMinted as MultiMintedEvent,
  RoleAdminChanged as RoleAdminChangedEvent,
  RoleGranted as RoleGrantedEvent,
  RoleRevoked as RoleRevokedEvent
} from "../generated/YokiDispatch/YokiDispatch"
import {
  MultiMinted,
  RoleAdminChanged,
  RoleGranted,
  RoleRevoked
} from "../generated/schema"

export function handleMultiMinted(event: MultiMintedEvent): void {
  let entity = new MultiMinted(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.from = event.params.from
  entity.yokiContract = event.params.yokiContract
  entity.partnerNftContract = event.params.partnerNftContract
  entity.to = event.params.to
  entity.omaAmount = event.params.omaAmount
  entity.partnerNFTTokenId = event.params.partnerNFTTokenId

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

